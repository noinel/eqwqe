package var;

public class Var01 {
	public static void main(String args[]) {
		//타입 = Type
		System.out.println("안녕 자바");//문자열 String
		System.out.println("7");//정수 int
		System.out.println("9.82");//실수 float, double
		System.out.println('가');//문자 char
		System.out.println(true);//boolean
		System.out.println(false);//참,거짓
		
		String name="백제원";
		System.out.println(name);
		
		int num=80;
		System.out.println(num);
		
		char ch='가';
		System.out.println(ch);
		
		boolean check=true;//경우의 수가 2가지
		System.out.println(check);
		
		double number=10;
		System.out.println(number);
	
	}
}